function actualizarFichaTecnica(indice) {
    
    var personaje = personajesFichaTecnica[indice];
    $('.classNombrePersonaje').html(personaje.nombre);
    $('.classApodoPersonaje').html(personaje.apodo);
    $('.classFechaPersonaje').html(personaje.fecha);
    $('.classInspiracionPersona').html(personaje.Inspiracion);
    $('.classLorePersonaje').html(personaje.Lore);
    if(indice != 0){
        $('.classHabilidadesPersonaje').html('<strong class = "classNegritaFooter">Habilidades: </strong>' + personaje.habilidades);
        $('.classPasatiempoPersonaje').html('<strong class = "classNegritaFooter">Pasatiempo: </strong>' + personaje.pasatiempo);
        $('.classEdadPersonaje').html('<strong class = "classNegritaFooter">Edad: </strong>' + personaje.edad );
        $('.classRegionPersonaje').html('<strong class = "classNegritaFooter">Región: </strong>' + personaje.region );
        $('.classComidaFavorita').html('<strong class = "classNegritaFooter">Comida favorita: </strong>' + personaje.comida );
        $('.classColorPersonaje').html('<strong class = "classNegritaFooter">Color favorito: </strong>' + personaje.color );
        $('.classSignoPersonaje').html('<strong class = "classNegritaFooter">Signo Zodiacal: </strong>' + personaje.signo );
    }else{
        $('.classHabilidadesPersonaje').html('');
        $('.classPasatiempoPersonaje').html('');
        $('.classEdadPersonaje').html('');
        $('.classRegionPersonaje').html('');
        $('.classComidaFavorita').html('');
        $('.classColorPersonaje').html('');
        $('.classSignoPersonaje').html('');
    }
}


$(document).ready(function(){

    

    // Carrusel 1
    $('#idCarruselPersonajesFotos').on('slid.bs.carousel', function (e) {
        var currentIndex = $(this).find('.carousel-item.active').index();

        // Elimina y vuelve a agregar el elemento para reiniciar la animación
        $('.classNombrePersonaje').each(function(){
            var $clone = $(this).clone();
            $(this).replaceWith($clone);
        });

        // Elimina y vuelve a agregar el elemento para reiniciar la animación
        $('.classEfectoDesaparecer').each(function(){
            var $clone = $(this).clone().removeClass('fadeInAnimation').addClass('fadeInAnimation');
            $(this).replaceWith($clone);
        });

        actualizarFichaTecnica(currentIndex);
    });


    // Mostrar los botones de prev y next cuando se haga hover sobre los carruseles
    $('.carruselPersonajes').hover(function() {
        $(this).find('.btn1Prev, .btn1Next, .btn2Next').show();
    }, function() {
        $(this).find('.btn1Prev, .btn1Next, .btn2Next').show();
    });

});



